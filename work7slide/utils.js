//冒泡排序
function bubbleSort(str) {
    let arr = str.split(',')
    let sortLog = []
    for (let i = 0; i < arr.length - 1; i++) {
        for (let j = 0; j < arr.length - i - 1; j++) {
            sortLog.push([arr.concat(),[j, j + 1]])
            sortLog.push([arr.concat(),[j, j + 1]])
            sortLog.push([arr.concat(),[j, j + 1]])          
        }
    }
}